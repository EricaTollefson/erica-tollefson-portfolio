		<h1>PWP Project: Milestone 2a</h1>
		<h2>Content Strategy</h2>
		<p>This project is a single page professional portfolio website to present examples of web development projects to potential clients. Each 		section will lay on a dark background with a slight graphic treatment (represented vaguely by a scribble line in the wireframes), and the text 		will fade onto the screen when each section is first loaded.</p>
		<h3>Header</h3>
		<p>The header will include an abstract photo that fades into the background, next to my name, title, and links to the other sections of the 		page.</p>
		<h3>About</h3>
		<p>The about section will include a brief description of my background, employment goals, and technical skills and technology expertise.</p>
		<h3>Portfolio</h3>
		<p>The portfolio section will feature a technical description of my capstone project, along with a picture of the site and a link to the 		site.</p>
		<h3>Contact</h3>
		<p>The contact section will feature a simple contact form, next to links to Github, Linkedin, and my resume.</p>
		<h3>Footer</h3>
		<p>The footer will include nav links (or a hamburger menu on mobile), along with icon links to Github and Linkedin.</p>
